import cv2
import matplotlib.pyplot as plt

def tampilkan_dengan_plt(img, title="Citra"):
    if len(img.shape) == 3:
        plt.imshow(cv2.cvtColor(img, cv2.COLOR_BGR2RGB))
    else:
        plt.imshow(img, cmap='gray')
    plt.title(title)
    plt.axis('off')
    plt.show()

def aplikasi_interaktif():
    plt.ion() # Aktifkan mode interaktif agar plt.show() tidak memblokir
    filepath = input("Masukkan nama file/path citra: ") # [cite: 233]
    img_asli = cv2.imread(filepath)

    if img_asli is None:
        print("Error: File tidak ditemukan!") # [cite: 243]
        return

    img_current = img_asli.copy()

    while True:
        print("\n--- Menu Manipulasi Citra ---") # [cite: 234]
        print("1: Informasi Citra") # [cite: 235]
        print("2: Konversi ke Grayscale") # [cite: 236]
        print("3: Resize") # [cite: 237]
        print("4: Rotasi") # [cite: 238]
        print("5: Crop") # [cite: 239]
        print("6: Simpan Hasil") # [cite: 240]
        print("7: Keluar") # [cite: 241]

        pilihan = input("Pilih menu (1-7): ")

        if pilihan == '1':
            print(f"Dimensi (H, W, C): {img_current.shape}")
            print(f"Tipe data: {img_current.dtype}")
            jml_channel = img_current.shape[2] if len(img_current.shape) == 3 else 1
            print(f"Jumlah channel: {jml_channel}")

        elif pilihan == '2':
            if len(img_current.shape) == 3:
                img_current = cv2.cvtColor(img_current, cv2.COLOR_BGR2GRAY)
                tampilkan_dengan_plt(img_current, "Hasil Grayscale") # [cite: 242]
            else:
                print("Citra sudah dalam format Grayscale.")

        elif pilihan == '3':
            w = int(input("Masukkan lebar baru (piksel): "))
            h = int(input("Masukkan tinggi baru (piksel): "))
            img_current = cv2.resize(img_current, (w, h))
            tampilkan_dengan_plt(img_current, f"Resize {w}x{h}") # [cite: 242]

        elif pilihan == '4':
            sudut = input("Masukkan sudut rotasi (90/180/270): ")
            if sudut == '90':
                img_current = cv2.rotate(img_current, cv2.ROTATE_90_CLOCKWISE)
            elif sudut == '180':
                img_current = cv2.rotate(img_current, cv2.ROTATE_180)
            elif sudut == '270':
                img_current = cv2.rotate(img_current, cv2.ROTATE_90_COUNTERCLOCKWISE)
            else:
                print("Sudut tidak valid!")
                continue
            tampilkan_dengan_plt(img_current, f"Rotasi {sudut}°") # [cite: 242]

        elif pilihan == '5':
            y1 = int(input("Koordinat y1 (baris mulai): "))
            y2 = int(input("Koordinat y2 (baris akhir): "))
            x1 = int(input("Koordinat x1 (kolom mulai): "))
            x2 = int(input("Koordinat x2 (kolom akhir): "))
            img_current = img_current[y1:y2, x1:x2]
            tampilkan_dengan_plt(img_current, "Hasil Crop") # [cite: 242]

        elif pilihan == '6':
            nama_simpan = input("Masukkan nama file tujuan (misal: hasil.jpg): ")
            cv2.imwrite(nama_simpan, img_current)
            print("Berhasil disimpan!")

        elif pilihan == '7':
            print("Terima kasih. Program selesai.")
            plt.ioff() # Nonaktifkan mode interaktif
            break

        else:
            print("Pilihan menu tidak tersedia.")

if __name__ == "__main__":
    aplikasi_interaktif()