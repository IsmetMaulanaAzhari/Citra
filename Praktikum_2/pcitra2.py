import cv2
import matplotlib.pyplot as plt

# Membaca citra
img = cv2.imread('bunga.jpg')

# Cek apakah citra berhasil dibaca
if img is None:
    print("Error: File tidak ditemukan!")
    exit()

# Menampilkan informasi citra
print(f"Tipe data: {img.dtype}")
print(f"Dimensi (tinggi, lebar, channel): {img.shape}")  # (h, w, c)

# Konversi BGR ke RGB agar tampil normal di matplotlib
img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

# Menampilkan citra
plt.imshow(img_rgb)
plt.title('Citra Asli (RGB)')
plt.axis('off')
plt.show()

"""Menampilkan Nilai Piksel"""

# Ambil nilai piksel pada baris ke-100, kolom ke-150 (format BGR)
pixel = img[100, 150]
print(f"Nilai piksel pada (100,150): B={pixel[0]}, G={pixel[1]}, R={pixel[2]}")

# Tampilkan nilai saluran merah pada area tertentu (misal: baris 50-100, kolom 50-100)
red_channel = img[50:100, 50:100, 2]  # indeks 2 adalah Red dalam BGR
print(f"Nilai Red pada area crop:\n{red_channel}")

"""Konversi ke Grayscale dan HSV"""

img_gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

# Konversi ke HSV
img_hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)

# Tampilkan ketiganya dalam satu figure
plt.figure(figsize=(12, 4))

plt.subplot(1, 3, 1)
plt.imshow(img_rgb)
plt.title('RGB')
plt.axis('off')

plt.subplot(1, 3, 2)
plt.imshow(img_gray, cmap='gray')
plt.title('Grayscale')
plt.axis('off')

plt.subplot(1, 3, 3)
plt.imshow(img_hsv)  # Matplotlib akan menampilkan HSV sebagai gambar berwarna (interpretasi salah), sebaiknya gunakan cv2.imshow() atau konversi dulu
plt.title('HSV (as is)')
plt.axis('off')

plt.tight_layout()
plt.show()

"""Mengubah Ukuran Citra (Resize)"""

# Resize menjadi 300x300 piksel
img_resize = cv2.resize(img_rgb, (300, 300))

# Resize dengan faktor skala (misal 0.5 dari ukuran asli)
height, width = img_rgb.shape[:2]
img_half = cv2.resize(img_rgb, (int(width*0.5), int(height*0.5)))

# Tampilkan hasil
plt.figure(figsize=(10, 4))
plt.subplot(1, 2, 1)
plt.imshow(img_resize)
plt.title('Resize 300x300')
plt.axis('off')

plt.subplot(1, 2, 2)
plt.imshow(img_half)
plt.title('Half Size')
plt.axis('off')
plt.show()

"""Rotasi dan Cropping"""

# Rotasi 90 derajat berlawanan arah jarum jam
img_rotated = cv2.rotate(img_rgb, cv2.ROTATE_90_COUNTERCLOCKWISE)

# Crop bagian tengah (misal: 100x100 dari pusat)
h, w = img_rgb.shape[:2]
start_row, start_col = h//2 - 50, w//2 - 50
img_crop = img_rgb[start_row:start_row+100, start_col:start_col+100]

# Tampilkan
plt.figure(figsize=(8, 4))
plt.subplot(1, 2, 1)
plt.imshow(img_rotated)
plt.title('Rotated 90°')
plt.axis('off')

plt.subplot(1, 2, 2)
plt.imshow(img_crop)
plt.title('Crop (100x100)')
plt.axis('off')
plt.show()

"""Menyimpan Citra"""

cv2.imwrite('bunga_gray.jpg', img_gray)
cv2.imwrite('bunga_rotated.jpg', cv2.cvtColor(img_rotated, cv2.COLOR_RGB2BGR))  # simpan dalam format BGR
print("Citra berhasil disimpan.")

