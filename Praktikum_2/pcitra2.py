import cv2
import matplotlib.pyplot as plt

def main():
    # Baca citra
    img = cv2.imread('bunga.jpg')
    if img is None:
        print("File tidak ditemukan!")
        return

    # Konversi BGR ke RGB untuk tampilan
    img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

    # 1. Informasi citra
    print(f"Dimensi: {img.shape}")
    print(f"Tipe data: {img.dtype}")

    # 2. Tampilkan citra asli
    plt.figure(figsize=(12, 6))
    plt.subplot(2, 3, 1)
    plt.imshow(img_rgb)
    plt.title('Asli (RGB)')
    plt.axis('off')

    # 3. Grayscale
    img_gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    plt.subplot(2, 3, 2)
    plt.imshow(img_gray, cmap='gray')
    plt.title('Grayscale')
    plt.axis('off')

    # 4. HSV (tampilkan saluran H saja sebagai contoh)
    img_hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)
    plt.subplot(2, 3, 3)
    plt.imshow(img_hsv[:,:,0], cmap='hsv')  # Hue channel
    plt.title('Hue Channel')
    plt.axis('off')

    # 5. Resize
    img_resize = cv2.resize(img_rgb, (200, 200))
    plt.subplot(2, 3, 4)
    plt.imshow(img_resize)
    plt.title('Resize 200x200')
    plt.axis('off')

    # 6. Rotasi
    img_rot = cv2.rotate(img_rgb, cv2.ROTATE_90_CLOCKWISE)
    plt.subplot(2, 3, 5)
    plt.imshow(img_rot)
    plt.title('Rotate 90° CW')
    plt.axis('off')

    # 7. Crop
    h, w = img_rgb.shape[:2]
    img_crop = img_rgb[h//4:3*h//4, w//4:3*w//4]
    plt.subplot(2, 3, 6)
    plt.imshow(img_crop)
    plt.title('Crop')
    plt.axis('off')

    plt.tight_layout()
    plt.show()

    # Simpan
    cv2.imwrite('bunga_gray.jpg', img_gray)
    cv2.imwrite('bunga_resize.jpg', cv2.cvtColor(img_resize, cv2.COLOR_RGB2BGR))
    print("Semua hasil telah disimpan.")

if __name__ == "__main__":
    main()

